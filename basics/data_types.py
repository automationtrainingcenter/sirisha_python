"""
variable is name of a memory location
data type is  classification of data
In Python we have
int
boolean
float
string

type() is used to get the type of the variable
"""

i = 1000
print(f'i = {i}')
print(type(i))


f = 10.234
print(f'f = {f}')
print(type(f))


bool = True
print(f'bool = {bool}')
print(type(bool))


name = 'sirisha'
print(f'name = {name}')
print(type(name))

char = 'a'
print(f'char = {char}')
print(type(char))
